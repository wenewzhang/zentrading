-module(zentrading_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    ok = lager:start(),
    lager:set_loglevel({lager_file_backend, "zentrading.log"}, error),
    zentrading_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

